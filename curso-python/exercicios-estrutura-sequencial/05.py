# Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.

print('--------------------')
print('Álgebra: Calculando Raio de um círculo!')
print('--------------------')

raio = float(input('Digite o raio de um círculo: '))

pi = 3.14

area = (pi * (raio**2))

print(f"A área do círculo equivale a {area}cm² aproximadamente.")